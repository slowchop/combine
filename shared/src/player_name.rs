#[derive(Clone, Copy, PartialEq, Debug, Default)]
pub struct PlayerName(u8, u8);

impl PlayerName {
    pub fn random() -> Self {
        PlayerName(thread_rng().gen(), thread_rng().gen())
    }

    pub fn to_string(&self) -> String {
        format!(
            "{} {}",
            FIRST_NAMES[self.0 as usize], LAST_NAMES[self.1 as usize]
        )
    }

    pub fn to_tuple(&self) -> (u8, u8) {
        (self.0, self.1)
    }
}

impl From<(u8, u8)> for PlayerName {
    fn from((first, second): (u8, u8)) -> Self {
        PlayerName(first, second)
    }
}

